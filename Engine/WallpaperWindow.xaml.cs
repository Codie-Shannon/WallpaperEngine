        //Windows
        public static IntPtr wDesktop;
        public static IntPtr wWorker;
        public static IntPtr wWallpaper;

        public WallpaperWindow()
        {
            InitializeComponent();
        }

        private void Window_Loaded(object sender, RoutedEventArgs e)
        {
            //Run Parent Wallpaper Method
            ParentwWallpaper();

            //Assign Size
            Height = SystemParameters.PrimaryScreenHeight;
            Width = SystemParameters.PrimaryScreenWidth;

            //Assign Position
            Top = SystemParameters.VirtualScreenTop;
            Left = SystemParameters.VirtualScreenLeft;
        }

        private void MediaElement_MediaEnded(object sender, RoutedEventArgs e)
        {
            //Set Wallpaper Position To 0
            me_Wallpaper.Position = TimeSpan.FromSeconds(0);
        }

        public static void ParentwWallpaper()
        {
            //Get Program Manager Window
            wDesktop = FindWindow("Progman", "Program Manager");

            //Send Message To Program Manager To Spawn Worker
            SendMessageTimeout(wDesktop, SPAWN_WORKERW, new IntPtr(0), IntPtr.Zero, SendMessageTimeoutFlags.SMTO_NORMAL, Timeout, out IntPtr result);

            //Get wWorker
            wWorker = GetNextWindow(wDesktop, GetWindowType.GW_HWNDPREV);

            //Get Wallpaper Window
            wWallpaper = new WindowInteropHelper(MainWindow.WallpaperRef).Handle;

            //Set wWallpaper Parent To wWorker
            SetParent(wWallpaper, wWorker);
        }

        public static void ChangeWallpaper()
        {
            MainWindow.WallpaperRef.me_Wallpaper.Source = new Uri(Properties.Settings.Default.Path);
        }
